/* ================= WORLD SETUP ================= */
function buildWorld(){
  engine = Engine.create({ enableSleeping: false });
  engine.gravity.y = GRAVITY_Y;
  world = engine.world;
  const opts = { isStatic: true, friction: FRICTION, restitution: 0 };
  /* VERY thick static bodies whose inner faces sit exactly at the
     visible walls — thin walls let fast bodies tunnel straight through
     on a janky first-load frame. Thickness costs nothing. */
  const T = 90;
  /* the 45-degree corner chamfers are tangent to the drawn rounded
     corner: inner face touches the visual arc at its midpoint, angled
     along (1,1) on the left / (1,-1) on the right, pushed OUTWARD by
     half their thickness so nothing invisible pokes into the play area */
  const Ri  = CORNER_R - WALL_T/2;          // interior corner radius
  const k   = Ri * (1 - Math.SQRT1_2);      // arc midpoint offset
  const out = (T/2) * Math.SQRT1_2;         // recess by half thickness
  Composite.add(world, [
    Bodies.rectangle((BOX_L+BOX_R)/2, BOX_BOTTOM + T/2, BOX_W + T*4, T, opts),
    Bodies.rectangle(IN_L - T/2, BOX_TOP - 400, T, (BOX_H + 900) * 2, opts),
    Bodies.rectangle(IN_R + T/2, BOX_TOP - 400, T, (BOX_H + 900) * 2, opts),
    Bodies.rectangle(IN_L + k - out, BOX_BOTTOM - k + out, Ri * 2.4, T, { ...opts, angle:  Math.PI/4 }),
    Bodies.rectangle(IN_R - k + out, BOX_BOTTOM - k + out, Ri * 2.4, T, { ...opts, angle: -Math.PI/4 }),
  ]);
  Events.on(engine, 'collisionStart', onCollisions);
  Events.on(engine, 'collisionActive', onCollisions);
  Events.on(engine, 'collisionStart', onFirstTouch);
}

function fruitBody(tier, x, y){
  const t = TIERS[tier];
  const common = {
    restitution: RESTITUTION, friction: FRICTION,
    frictionStatic: FRICTION_STATIC, frictionAir: AIR_FRICTION,
    density: 0.0012,
  };
  let b;
  if(t.flower){
    /* the flower collides with its REAL shape: a compound of the
       center disc + six petal discs (matches the logo silhouette). */
    const r = t.r;
    const parts = [Bodies.circle(x, y, r*0.52, common)];
    for(let k = 0; k < 6; k++){
      const a = k * Math.PI/3 + Math.PI/6;
      parts.push(Bodies.circle(x + Math.cos(a)*r*0.55, y + Math.sin(a)*r*0.55, r*0.43, common));
    }
    b = Body.create({ parts, restitution: RESTITUTION, friction: FRICTION,
      frictionStatic: FRICTION_STATIC, frictionAir: AIR_FRICTION });
  }else{
    b = Bodies.circle(x, y, t.r, common);
  }
  b.tier = tier;
  b.merging = false;
  b.bornAt = performance.now();
  return b;
}

/* ================= MERGING ================= */
function onCollisions(ev){
  if(over) return;
  for(const pair of ev.pairs){
    /* compound bodies (the flower) report their PARTS — resolve up */
    const a = pair.bodyA.parent || pair.bodyA;
    const b = pair.bodyB.parent || pair.bodyB;
    if(a.tier === undefined || b.tier === undefined) continue;
    if(a === b) continue;
    if(a.tier !== b.tier) continue;
    if(a.merging || b.merging) continue;
    a.merging = b.merging = true;
    const tier = a.tier;
    const mx = (a.position.x + b.position.x) / 2;
    const my = (a.position.y + b.position.y) / 2;
    const vx = (a.velocity.x + b.velocity.x) / 2;
    const vy = (a.velocity.y + b.velocity.y) / 2;
    removeFruit(a); removeFruit(b);

    if(tier === TIERS.length - 1){
      addScore(FLOWER_BONUS, mx, my);   /* the jackpot is the jackpot — flat */
      celebrate(mx, my);
      sndBig();
      shake = reduceMotion ? 0 : 14;
      continue;
    }
    const nt = tier + 1;
    bumpCollect(nt);   /* the aquarium remembers every creature ever made */
    const maxMade0 = maxMade;
    if(nt > maxMade){
      maxMade = nt;
      store.set(KEY_SEEN, maxMade);
    }
    const unlock = nt > maxMade0 && nt >= SECRET_FROM;
    const nb = fruitBody(nt, mx, my);
    Body.setVelocity(nb, { x: vx, y: vy });
    Composite.add(world, nb);
    bodies.push(nb);
    popTweens.set(nb.id, performance.now());
    /* chain merges inside the window build a multiplier */
    const nowMs = performance.now();
    combo = (nowMs - comboAt < COMBO_WINDOW_MS) ? Math.min(COMBO_CAP, combo + 1) : 1;
    comboAt = nowMs;
    if(nt > runBestTier) runBestTier = nt;
    addScore(MERGE_POINTS[nt] * combo, mx, my - TIERS[nt].r);
    /* SPLASH: the drop merged the instant it landed — pure skill, paid
       as a clean double of the merge. An undefined touchedAt means this
       merge IS the body's first contact (the merge listener can run
       before the touch listener in the same collision batch) — the
       purest splash of all. Spawned/test bodies never carry byPlayer,
       so automated scoring stays exact. */
    if(mechOn && [a, b].some(x => x.byPlayer && (x.touchedAt === undefined || nowMs - x.touchedAt <= 350))){
      splashes++;
      addScore(MERGE_POINTS[nt] * combo, mx, my - TIERS[nt].r - 20);
      popups.push({ x: mx, y: my - TIERS[nt].r - 48, n: 0, t: 0, txt: 'SPLASH!', big: true });
      for(let i = 0; i < 6 && particles.length < MAX_PARTICLES; i++){
        const an = Math.random() * Math.PI * 2, sp = 1 + Math.random() * 2;
        particles.push({ x: mx, y: my, vx: Math.cos(an) * sp, vy: Math.sin(an) * sp - 1, r: 2, color: '#F6FBF6', life: .6 });
      }
    }
    if(combo >= 2){
      popups.push({ x: mx, y: my - TIERS[nt].r - 26, n: 0, t: 0,
        txt: 'COMBO x' + combo, big: true, gold: combo >= 4 });
    }
    if(combo >= 4){
      goldFlashAt = nowMs;
      showToast('COMBO x' + combo + '!!', true);
    }
    if(unlock){
      const unm = TIERS[nt].flower ? 'THE FLOWER BLOOMS' : 'THE ' + TIERS[nt].name.toUpperCase() + ' JOINS THE SEA';
      showToast(unm, false);
    }
    burst(mx, my, TIERS[nt].fill || '#D70000', Math.min(12, 5 + nt));
    if(!reduceMotion) rings.push({ x: mx, y: my, r: TIERS[nt].r * .7, t: 0, color: TIERS[nt].fill || '#D70000' });
    if(unlock){
      /* a brand-new creature: gold sparkle on its legend slot */
      const lx = SCENE_W/2 - LEGEND_GAP * (TIERS.length - 1) / 2 + nt * LEGEND_GAP;
      burst(lx, LEGEND_Y, '#E8B33C', 12);
      rings.push({ x: lx, y: LEGEND_Y, r: LEGEND_R, t: 0, color: '#E8B33C' });
    }
    sndMerge(nt + (combo - 1) * 2);
    if(nt >= SHAKE_TIER && !reduceMotion) shake = Math.max(shake, 6);
  }
}
/* a tiny dust puff the first time a creature touches anything */
function onFirstTouch(ev){
  for(const pair of ev.pairs){
    for(const raw of [pair.bodyA, pair.bodyB]){
      const b = raw.parent || raw;
      if(b.tier === undefined || b.puffed) continue;
      b.puffed = true;
      b.touchedAt = performance.now();   // splash-bonus clock, always recorded
      if(reduceMotion) continue;         // the dust below is visual only
      if(b.speed < 3.2) continue;
      b.squashAt = performance.now();
      const s = pair.collision && pair.collision.supports && pair.collision.supports[0];
      const px = s ? s.x : b.position.x, py = s ? s.y : b.position.y + TIERS[b.tier].r;
      for(let i = 0; i < 4 && particles.length < MAX_PARTICLES; i++){
        const a = -Math.PI/2 + (Math.random() - .5) * 2.2, sp = .6 + Math.random() * 1.4;
        particles.push({ x: px, y: py, vx: Math.cos(a)*sp, vy: Math.sin(a)*sp * .5, r: 1.5 + Math.random()*2, color: 'rgba(107,104,98,.55)', life: .5 });
      }
    }
  }
}
