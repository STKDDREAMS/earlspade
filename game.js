function rollSpawnTier(){
  const total = SPAWN_WEIGHTS.reduce((a,b)=>a+b,0);
  let r = Math.random() * total;
  for(let i = 0; i <= MAX_SPAWN_TIER; i++){ r -= SPAWN_WEIGHTS[i]; if(r < 0) return i; }
  return 0;
}
