function buildSprite(t){
  const cv = document.createElement('canvas');
  cv.width = cv.height = GRID * CELL;
  const g = cv.getContext('2d');
  const cpx = GRID * CELL / 2;
  const Rpx = DISC_R * CELL;
  const ring = CELL * 1.15;

  const grad = g.createRadialGradient(cpx - Rpx*0.35, cpx - Rpx*0.35, Rpx*0.15, cpx, cpx, Rpx);
  grad.addColorStop(0, t.lt);
  grad.addColorStop(0.55, t.fill);
  grad.addColorStop(1, t.dk);
  g.beginPath(); g.arc(cpx, cpx, Rpx - ring/2, 0, Math.PI*2);
  g.fillStyle = grad; g.fill();
  g.lineWidth = ring;
  g.strokeStyle = INK;
  g.stroke();

  const cell = (x, y, col) => { g.fillStyle = col; g.fillRect(x*CELL, y*CELL, CELL, CELL); };

  function stamp(f, clip){
    if(!f) return;
    if(clip){
      g.save();
      g.beginPath(); g.arc(cpx, cpx, Rpx - ring, 0, Math.PI*2);
      g.clip();
    }
    for(let ry = 0; ry < f.rows.length; ry++){
      const row = f.rows[ry];
      for(let rx = 0; rx < row.length; rx++){
        const ch = row[rx];
        if(ch === '.') continue;
        const x = f.ox + rx, y = f.oy + ry;
        if(x < 0 || y < 0 || x >= GRID || y >= GRID) continue;
        if(ch === 'X'){ g.clearRect(x*CELL, y*CELL, CELL, CELL); continue; }
        cell(x, y, PAL[ch] || INK);
      }
    }
    if(clip) g.restore();
  }

  /* base markings first, then the shared cute face, then overlays */
  stamp(t.featClip, true);
  stamp(t.feat, false);
  if(t.face !== false){
    const ey = 13 + (t.eyeY || 0);
    for(const ex of [12, 21]){
      for(let yy = 0; yy < 4; yy++){
        cell(ex, ey+yy, INK); cell(ex+1, ey+yy, INK); cell(ex+2, ey+yy, INK);
      }
      cell(ex, ey, '#FFFFFF'); cell(ex, ey+1, '#FFFFFF');   // big glint
      cell(ex+2, ey+2, '#FFFFFF');                          // sparkle
    }
    if(t.blush !== false){
      const bl = t.blushC || '#F2B8C6';
      cell(7, ey+4, bl); cell(8, ey+4, bl);
      cell(27, ey+4, bl); cell(28, ey+4, bl);
    }
  }
  stamp(t.feat2, false);
  return cv;
}
