/* the vessel: pixel-shadowed rounded box in the site's dialog style */
function vesselPath(inset){
  const l = BOX_L + inset, r = BOX_R - inset, b = BOX_BOTTOM + WALL_T - inset, t = BOX_TOP;
  const cr = Math.max(6, CORNER_R - inset);
  ctx.beginPath();
  ctx.moveTo(l, t);
  ctx.lineTo(l, b - cr);
  ctx.quadraticCurveTo(l, b, l + cr, b);
  ctx.lineTo(r - cr, b);
  ctx.quadraticCurveTo(r, b, r, b - cr);
  ctx.lineTo(r, t);
}
function render(now){
  ctx.fillStyle = '#E9E6DF';
  ctx.fillRect(0, 0, cssW, cssH);
  ctx.save();
  if(shake > 0){
    ctx.translate((Math.random()-.5) * shake, (Math.random()-.5) * shake);
    shake *= 0.85; if(shake < .4) shake = 0;
  }
  ctx.save();
  ctx.translate(offX, offY);
  ctx.scale(scale, scale);

  /* hard offset shadow (same language as the site's pixel dialogs) */
  ctx.save();
  ctx.translate(6, 6);
  vesselPath(0);
  ctx.fillStyle = 'rgba(22,20,18,.16)';
  ctx.fill();
  ctx.restore();

  /* interior: slightly deeper paper */
  vesselPath(WALL_T/2);
  ctx.fillStyle = '#E2DDD2';
  ctx.fill();

  /* ambient bubbles + danger glow live INSIDE the vessel */
  ctx.save();
  vesselPath(WALL_T/2);
  ctx.clip();
  for(const a of ambient){
    a.y -= a.sp;
    if(a.y < BOX_TOP + 14){ a.y = BOX_BOTTOM - 8; a.x = IN_L + 18 + Math.random() * (IN_R - IN_L - 36); }
    const ax = a.x + Math.sin(now / 900 + a.ph) * 4;
    ctx.beginPath(); ctx.arc(ax, a.y, a.r, 0, Math.PI * 2);
    ctx.strokeStyle = 'rgba(107,104,98,.18)';
    ctx.lineWidth = 1.2;
    ctx.stroke();
  }
  if(anyOverLine && !over){
    const g = ctx.createLinearGradient(0, BOX_TOP, 0, BOX_TOP + 130);
    const a = 0.10 + 0.07 * Math.sin(now / 130);
    g.addColorStop(0, 'rgba(215,0,0,' + a.toFixed(3) + ')');
    g.addColorStop(1, 'rgba(215,0,0,0)');
    ctx.fillStyle = g;
    ctx.fillRect(BOX_L, BOX_TOP, BOX_W, 130);
  }
  ctx.restore();

  /* game-over line — subtle dashes; red flash while threatened */
  const lineY = BOX_TOP + OVER_LINE_OFF;
  ctx.save();
  ctx.setLineDash([8, 8]);
  ctx.lineWidth = 2;
  ctx.strokeStyle = anyOverLine
    ? (Math.floor(now / 180) % 2 ? 'rgba(215,0,0,.85)' : 'rgba(215,0,0,.25)')
    : 'rgba(22,20,18,.18)';
  ctx.beginPath(); ctx.moveTo(IN_L, lineY); ctx.lineTo(IN_R, lineY); ctx.stroke();
  ctx.restore();

  /* aim guide + held creature */
  if(!over){
    const hx = clampAim(aimX), hr = TIERS[heldTier].r;
    const hy = heldY() + (reduceMotion ? 0 : Math.sin(now / 480) * 3);
    ctx.save();
    ctx.globalAlpha = 0.25;
    ctx.setLineDash([4, 8]);
    ctx.strokeStyle = INK; ctx.lineWidth = 1.5;
    ctx.beginPath(); ctx.moveTo(hx, hy + hr + 4); ctx.lineTo(hx, BOX_BOTTOM - 2); ctx.stroke();
    ctx.restore();
    ctx.globalAlpha = canDrop ? 1 : 0.45;
    drawFruitAt(ctx, hx, hy, hr, heldTier, 0);
    ctx.globalAlpha = 1;
  }

  /* creatures (with scale-pop on fresh merges) */
  for(const b of bodies){
    let r = TIERS[b.tier].r;
    const born = popTweens.get(b.id);
    if(born !== undefined){
      const p = (now - born) / 220;
      if(p >= 1){ popTweens.delete(b.id); }
      else { r *= 1 + 0.22 * Math.sin(Math.min(1, p) * Math.PI); }
    }
    drawFruitAt(ctx, b.position.x, b.position.y, r, b.tier, b.angle);
  }

  /* the vessel walls: ink stroke with rounded bottom corners + rim lips */
  vesselPath(WALL_T/2);
  ctx.lineWidth = WALL_T;
  ctx.strokeStyle = INK;
  ctx.stroke();
  ctx.fillStyle = INK;
  ctx.fillRect(BOX_L - 4, BOX_TOP, WALL_T + 8, 4);
  ctx.fillRect(BOX_R - WALL_T - 4, BOX_TOP, WALL_T + 8, 4);

  /* THE LEGEND — the chain under the box; the last four keep their
     secret behind a gold "?" until each is created. */
  {
    const startX = SCENE_W/2 - LEGEND_GAP * (TIERS.length - 1) / 2;
    ctx.imageSmoothingEnabled = true;
    for(let i = 0; i < TIERS.length; i++){
      const lx = startX + i * LEGEND_GAP;
      const secret = i >= SECRET_FROM && i > maxMade;
      if(secret){
        const special = i >= TIERS.length - 2;   // the final two mysteries
        const d = LEGEND_R * 2 * SPRITE_OVER;
        if(special){
          /* gold box, breathing glow, two orbiting sparks — clearly
             something worth chasing (without saying a word) */
          const pulse = reduceMotion ? 1 : 1 + 0.09 * Math.sin(now / 260 + i * 2);
          ctx.save();
          ctx.globalAlpha = 0.28 + (reduceMotion ? 0 : 0.16 * Math.sin(now / 260 + i * 2));
          ctx.strokeStyle = '#E8B33C';
          ctx.lineWidth = 2.5;
          ctx.beginPath(); ctx.arc(lx, LEGEND_Y, LEGEND_R * 1.55 + (reduceMotion ? 0 : Math.sin(now / 260 + i * 2) * 1.6), 0, Math.PI * 2); ctx.stroke();
          ctx.restore();
          const dd = d * pulse;
          ctx.drawImage(SECRET_GOLD_SPRITE, lx - dd/2, LEGEND_Y - dd/2, dd, dd);
          if(!reduceMotion){
            ctx.fillStyle = '#E8B33C';
            for(const k of [0, 1]){
              const ang = now / 650 + i + k * Math.PI;
              const sx = lx + Math.cos(ang) * LEGEND_R * 1.8;
              const sy = LEGEND_Y + Math.sin(ang) * LEGEND_R * 1.8;
              ctx.fillRect(sx - 1.5, sy - 1.5, 3, 3);
            }
          }
        }else{
          ctx.drawImage(SECRET_SPRITE, lx - d/2, LEGEND_Y - d/2, d, d);
        }
      }else{
        drawFruitAt(ctx, lx, LEGEND_Y, LEGEND_R, i, 0);
      }
    }
  }

  /* expanding merge rings */
  for(let i = rings.length - 1; i >= 0; i--){
    const rg = rings[i];
    rg.t += 0.055;
    if(rg.t >= 1){ rings.splice(i, 1); continue; }
    ctx.globalAlpha = (1 - rg.t) * 0.55;
    ctx.lineWidth = 3 - rg.t * 2;
    ctx.strokeStyle = rg.color;
    ctx.beginPath(); ctx.arc(rg.x, rg.y, rg.r + rg.t * 46, 0, Math.PI * 2); ctx.stroke();
  }
  ctx.globalAlpha = 1;

  /* particles (square = pixel confetti) */
  for(let i = particles.length - 1; i >= 0; i--){
    const p = particles[i];
    p.x += p.vx; p.y += p.vy; p.vy += 0.12; p.life -= 0.025;
    if(p.life <= 0){ particles.splice(i, 1); continue; }
    ctx.globalAlpha = Math.min(1, p.life);
    ctx.fillStyle = p.color;
    ctx.fillRect(p.x - p.r/2, p.y - p.r/2, p.r, p.r);
  }
  ctx.globalAlpha = 1;

  /* score popups */
  ctx.font = '13px "Press Start 2P", monospace';
  ctx.textAlign = 'center';
  for(let i = popups.length - 1; i >= 0; i--){
    const p = popups[i];
    p.t += 0.02;
    if(p.t >= 1){ popups.splice(i, 1); continue; }
    ctx.globalAlpha = 1 - p.t;
    ctx.fillStyle = INK;
    ctx.fillText('+' + p.n, p.x, p.y - p.t * 46);
  }
  ctx.globalAlpha = 1;
  ctx.restore();
  ctx.restore();
}

/* ================= MAIN LOOP ================= */
function loop(now){
  rafId = requestAnimationFrame(loop);
  if(!lastT) lastT = now;
  let dt = now - lastT;
  lastT = now;
  if(dt > 100) dt = 100;
  acc += dt;
  let steps = 0;
  while(acc >= STEP && steps < 3){
    Engine.update(engine, STEP);
    checkOverLine(STEP / 1000);
    acc -= STEP; steps++;
  }
  if(steps === 3) acc = 0;
  /* belt-and-braces: if anything ever escapes the vessel (a pathological
     frame spike), lift it gently back in rather than losing it */
  for(const b of bodies){
    const r = TIERS[b.tier].r;
    if(b.position.y - r > BOX_BOTTOM + 4 || b.position.x < IN_L - r - 40 || b.position.x > IN_R + r + 40){
      Body.setPosition(b, { x: Math.max(IN_L + r, Math.min(IN_R - r, b.position.x)), y: BOX_TOP + r + 10 });
      Body.setVelocity(b, { x: 0, y: 0 });
      b.bornAt = performance.now();
    }
  }
  tickScore();
  render(now);
}
function pause(){ if(rafId){ cancelAnimationFrame(rafId); rafId = null; lastT = 0; acc = 0; } }
function resume(){ if(!rafId && running) rafId = requestAnimationFrame(loop); }
document.addEventListener('visibilitychange', () => { document.hidden ? pause() : resume(); });

